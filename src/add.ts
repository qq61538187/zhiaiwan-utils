/**
 * Returns the sum of two numbers.
 *
 * @since +0.1.0
 * @category Math
 * @param {number} a The first addend.
 * @param {number} b The second addend.
 * @returns {number} Returns the arithmetic sum.
 * @example
 *
 * add(1, 2)
 * // => 3
 */
export function add(a: number, b: number): number {
  return a + b
}

export default add
